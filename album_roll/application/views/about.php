	<div class="list_items">
		<div class="list_item">
			<div class="instructions">
			There are a lot of great sites out there aimed at taste exploration and music discovery, but most of these focus on comparing artists or individual tracks. As such, listening to music in the album format has become somewhat obsolete in this digital age of sharing mp3s and blogging buzzbands, and there's not much out there for those of us who like to listen to records in the way they're intended to be heard.
<br />
<br />
Album Roll aims to solve that problem by providing a user-friendly and visually pleasing interface for creating and sharing lists of albums. Users can construct lists according to their all-time favorites, what they're currently listening to, the best of a year or decade, or a general theme of their choosing, all the while populating a database of albums, artists, labels, reviews, and links that make it easy to search for and explore related content. Based on the groupings of albums in these lists, the site makes customizable recommendations specific to the user or by album or list. Additionally, Album Roll gives users the ability to comment on and rate both individual albums and user-made lists, both to stir up healthy discussion and offer further means of discovery.
			</div>
		</div>
		<div class="sub_content_header">
			guidelines
		</div>
		<div class="list_item">
			<div class="instructions">
			Since our album database consists solely of those uploaded by our users, it's up to all of us to ensure that the information we provide is accurate and helpful to our peers. Please ensure that any details, comments, or links you add are appropriate (and legal). 
			<br />
			<br />
			Additionally, part of the purpose of the site is to provide an appealing aesthetic for viewing the lists that users create. To preserve this aesthetic, we require that users upload album art for any albums that they add to the database. Please use common sense and be respectful to other users when uploading content.
			</div>
